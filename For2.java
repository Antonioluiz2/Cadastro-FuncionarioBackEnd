package estruturaRepeticao;

public class For2 {

	public static void main(String[] args) {
		
		int x=3;
		int y=0;
		int v=0;
		for(int i=0;i<x;i++) {
			System.out.print(i +",");
			y=y+5;
			System.out.println(y);
			
			v=y+10;
			System.out.println(v);
			System.out.println("===============");
		}
		System.out.println("Hello Luiz!");
	}

}
